// Define a function that takes in two numbers and returns their sum
function sumNumbers(num1, num2) {
	// Add the two numbers together and return the result
	return num1 + num2;
}

// Call the sumNumbers() function and output the result to the console
console.log("The sum of 5 and 10 is: " + sumNumbers(5, 10));

